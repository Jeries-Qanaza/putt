const I18nContext = createContext();

export function I18nProvider({ children }) {
  const [lang, setLang] = useState(() => localStorage.getItem('putt_lang') || 'he');

  useEffect(() => {
    localStorage.setItem('putt_lang', lang);
    document.documentElement.dir = translations[lang].dir;
    document.documentElement.lang = lang;
  }, [lang]);

  const t = (key) => translations[lang]?.[key] || translations.en[key] || key;
  const dir = translations[lang]?.dir || 'ltr';
  const isRTL = dir === 'rtl';

  const getLocalizedField = (item, field) => {
    if (!item) return '';
    if (lang === 'he' && item[`${field}_he`]) return item[`${field}_he`];
    if (lang === 'ar' && item[`${field}_ar`]) return item[`${field}_ar`];
    return item[field] || '';
  };

  return (
    <I18nContext.Provider value={{ lang, setLang, t, dir, isRTL, getLocalizedField }}>
      {children}
    </I18nContext.Provider>
  );
}

export function useI18n() {
  return useContext(I18nContext);
}
